// №4 Написать метод, которому в качестве аргументов передается строка и число,
// метод должен отпечатать в консоль указанную строку, указанное количество раз.
    private static void printMessage(int count, String message) {
        System.out.println("Задание №4. Сообщение должно быть выведено " + (count) + " раз");
        int value = 0;
        while (value < count) {
            System.out.println(message + " - " + (value + 1));
            value++;
        }
    }

// 5. Написать метод, который определяет, является ли год високосным, и возвращает boolean
