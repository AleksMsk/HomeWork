// Високосным годом является год, номер которого делится без остатка на 4,
// кроме тех, числа которых оканчиваются на два нуля, но не делятся на 400, поэтому, например, годы 1700, 1800,1900 не являются високосными.
//Год високосный, если он делится на четыре без остатка. Но если он делится на 100 без остатка,
// это не високосный год. Однако, если он делится без остатка на 400, это високосный год.
//Таким образом, 2000 г. является особым високосным годом, который бывает лишь раз в 400 лет.

    private static boolean checkYear(int a) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Задание №5*. Введите год: ");
        int input_year = sc.nextInt();
        int flag1 = (input_year % 4);
        int flag2 = (input_year % 100);
        int flag3 = (input_year % 400);
        if (flag1 == 0 && flag2 != 0 || flag1 == 0 && flag3 == 0) {
            return (true);
        }else {
            return (false);
        }
    }
